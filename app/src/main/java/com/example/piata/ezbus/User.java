package com.example.piata.ezbus;

class User {

    private String Name;
    private String Surname;
    private String Age;
    private String Email;
    private String Username;

    public User(String name, String surname, String age, String email, String username) {
        Name = name;
        Surname = surname;
        Age = age;
        Email = email;
        Username = username;
    }

}