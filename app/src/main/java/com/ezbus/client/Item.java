package com.ezbus.client;

class Item {

    String id;
    String companyId;
    double price;
    int expiration;


    public String getId() {
        return this.id;
    }

    public String getCompanyId() {
        return this.companyId;
    }

    public double getPrice() {
        return this.price;
    }

    public int getExpiration() {
        return this.expiration;
    }

}